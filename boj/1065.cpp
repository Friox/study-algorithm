#include <stdio.h>
int main() {
    int arr[144] = { 0 }, index = 99, N;
    int num[9][5] = {
        {111, 123, 135, 147, 159},
        {210, 222, 234, 246, 258},
        {321, 333, 345, 357, 369},
        {420, 432, 444, 456, 468},
        {531, 543, 555, 567, 579},
        {630, 642, 654, 666, 678},
        {741, 753, 765, 777, 789},
        {840, 852, 864, 876, 888},
        {951, 963, 975, 987, 999}
    };
    for (int i = 1; i <= 99; i++) arr[i - 1] = i;
    for (int i = 0; i < 9; i++) {
        for (int j = 0; j < 5; j++) arr[index++] = num[i][j];
    }
    index = 0;
    scanf("%d", &N);
    for (int i = 0; i < 144; i++) {
        if (arr[i] <= N) index++;
        else break;
    }
    printf("%d", index);
}